package ghidra.pdb.pdbreader.type;

import ghidra.pdb.PdbByteReader;
import ghidra.pdb.PdbException;
import ghidra.pdb.pdbreader.*;

/**
 * A class for a specific PDB data type.
 * <P>
 * For more information about PDBs, consult the Microsoft PDB API, see
 * <a href="https://devblogs.microsoft.com/cppblog/whats-inside-a-pdb-file">
 * What's inside a PDB File</a>.
 */
public class DerivedClassList16MsType extends AbstractDerivedClassListMsType {

	public static final int PDB_ID = 0x0205;

	/**
	 * Constructor for this type.
	 * @param pdb {@link AbstractPdb} to which this type belongs.
	 * @param reader {@link PdbByteReader} from which this type is deserialized.
	 * @throws PdbException Upon not enough data left to parse.
	 */
	public DerivedClassList16MsType(AbstractPdb pdb, PdbByteReader reader) throws PdbException {
		super(pdb, reader);
	}

	@Override
	public int getPdbId() {
		return PDB_ID;
	}

	@Override
	protected void parseTypeIndexList(PdbByteReader reader) throws PdbException {
		int count = reader.parseUnsignedShortVal();
		for (int i = 0; i < count; i++) {
			AbstractTypeIndex typeIndex = new TypeIndex16();
			typeIndex.parse(reader);
			typeIndexList.add(typeIndex);
		}
	}

}
